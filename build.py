#!/usr/bin/env python3
"""
build.py — turn rants/*.md into static HTML.

Workflow:
    1. write a new rant as  rants/<slug>.md  (see rants/README.md for the format)
    2. run                  python3 build.py
    3. it regenerates       rants/<slug>.html  (one per post)
       and                  rants.html         (the index, newest first)

No dependencies — standard library only. The rest of the site
(index.html, resume.html, work.html) is hand-written and untouched.
"""

import os
import re
import glob

HERE = os.path.dirname(os.path.abspath(__file__))
RANTS_DIR = os.path.join(HERE, "rants")

MONTHS = ["January", "February", "March", "April", "May", "June",
          "July", "August", "September", "October", "November", "December"]

GENERATED = "<!-- generated by build.py, edit the .md source, not this file -->"


# ----------------------------------------------------------------------
# helpers
# ----------------------------------------------------------------------
def escape(s):
    return s.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")


def attr(s):
    return escape(s).replace('"', "&quot;")


def fmt_date(d):
    """2026-07-17 -> 'July 17, 2026' (falls back to the raw string)."""
    try:
        y, m, day = d.split("-")
        return "%s %d, %s" % (MONTHS[int(m) - 1], int(day), int(y))
    except Exception:
        return d


# ----------------------------------------------------------------------
# markdown -> html  (a focused subset: enough for prose)
# ----------------------------------------------------------------------
def inline(text):
    codes = []

    def stash(m):
        codes.append(escape(m.group(1)))
        return "\x00%d\x00" % (len(codes) - 1)

    text = re.sub(r"`([^`]+)`", stash, text)       # protect inline code
    text = escape(text)                             # escape everything else
    text = re.sub(r"\[([^\]]+)\]\(([^)\s]+)\)",     # [text](url)
                  lambda m: '<a href="%s">%s</a>' % (m.group(2), m.group(1)), text)
    text = re.sub(r"\*\*([^*]+)\*\*", r"<strong>\1</strong>", text)
    text = re.sub(r"(?<!\*)\*([^*\n]+)\*(?!\*)", r"<em>\1</em>", text)
    text = re.sub(r"(?<!\w)_([^_\n]+)_(?!\w)", r"<em>\1</em>", text)
    text = re.sub(r"\x00(\d+)\x00",                 # restore inline code
                  lambda m: "<code>%s</code>" % codes[int(m.group(1))], text)
    return text


def md_to_html(md):
    lines = md.split("\n")
    out = []
    para = []
    i, n = 0, len(lines)

    def flush():
        if para:
            text = " ".join(para).strip()
            if text:
                out.append("<p>" + inline(text) + "</p>")
            para.clear()

    while i < n:
        line = lines[i]
        s = line.strip()

        if s.startswith("```"):                     # fenced code block
            flush()
            code, i = [], i + 1
            while i < n and not lines[i].strip().startswith("```"):
                code.append(lines[i])
                i += 1
            i += 1
            out.append("<pre><code>" + escape("\n".join(code)) + "</code></pre>")
            continue

        if s == "":
            flush()
            i += 1
            continue

        if re.match(r"^(-{3,}|\*{3,}|_{3,})$", s):   # horizontal rule
            flush()
            out.append("<hr />")
            i += 1
            continue

        m = re.match(r"^(#{1,6})\s+(.*)$", s)        # heading
        if m:
            flush()
            lvl = len(m.group(1))
            out.append("<h%d>%s</h%d>" % (lvl, inline(m.group(2).strip()), lvl))
            i += 1
            continue

        if s.startswith(">"):                        # blockquote
            flush()
            quote = []
            while i < n and lines[i].strip().startswith(">"):
                quote.append(re.sub(r"^\s*>\s?", "", lines[i]).strip())
                i += 1
            out.append("<blockquote>" + inline(" ".join(quote)) + "</blockquote>")
            continue

        if re.match(r"^([-*+]|\d+\.)\s+", s):        # list (ordered or not)
            flush()
            ordered = bool(re.match(r"^\d+\.\s+", s))
            mine = r"^\d+\.\s+" if ordered else r"^[-*+]\s+"
            other = r"^[-*+]\s+" if ordered else r"^\d+\.\s+"
            items = []
            while i < n:
                cs = lines[i].strip()
                if cs == "":                         # blank line ends the list
                    i += 1
                    break
                if re.match(mine, cs):               # new item of this list
                    items.append(re.sub(mine, "", cs))
                elif re.match(other, cs):            # a different list starts
                    break
                elif items:                          # lazy continuation of an item
                    items[-1] += " " + cs
                else:
                    break
                i += 1
            tag = "ol" if ordered else "ul"
            out.append("<%s>%s</%s>" % (
                tag, "".join("<li>%s</li>" % inline(x) for x in items), tag))
            continue

        para.append(s)
        i += 1

    flush()
    return "\n".join(out)


# ----------------------------------------------------------------------
# frontmatter
# ----------------------------------------------------------------------
def parse(md_text):
    meta, body = {}, md_text
    if md_text.startswith("---"):
        parts = md_text.split("---", 2)
        if len(parts) >= 3:
            for row in parts[1].strip().split("\n"):
                if ":" in row:
                    k, v = row.split(":", 1)
                    meta[k.strip()] = v.strip()
            body = parts[2].lstrip("\n")
    return meta, body


# ----------------------------------------------------------------------
# templates
# ----------------------------------------------------------------------
