EMPTY_LIST = ('    <p class="lede" style="margin-top:0;color:var(--mute)">'
              'Nothing published yet. Check back soon.</p>')


# ----------------------------------------------------------------------
# build
# ----------------------------------------------------------------------
def build():
    posts = []
    for path in sorted(glob.glob(os.path.join(RANTS_DIR, "*.md"))):
        name = os.path.basename(path)
        if name.lower() == "readme.md":
            continue
        with open(path, encoding="utf-8") as f:
            meta, body = parse(f.read())
        if not meta.get("title"):
            print("  skip (no title frontmatter): %s" % name)
            continue

        slug = os.path.splitext(name)[0]
        title = meta["title"]
        date = meta.get("date", "")
        summary = meta.get("summary", "")
        date_display = fmt_date(date)
        desc = summary or title

        html = POST_TEMPLATE.format(
            generated=GENERATED,
            fonts=FONTS,
            footer=FOOTER,
            title=escape(title),
            desc=attr(desc),
            date_display=escape(date_display),
            body=md_to_html(body),
        )
        with open(os.path.join(RANTS_DIR, slug + ".html"), "w", encoding="utf-8") as f:
            f.write(html)
        posts.append({"slug": slug, "title": title, "date": date,
                      "date_display": date_display, "summary": summary})
        print("  wrote rants/%s.html" % slug)

    # remove generated pages whose .md source is gone (renamed / deleted)
    built = {p["slug"] for p in posts}
    for html in glob.glob(os.path.join(RANTS_DIR, "*.html")):
        slug = os.path.splitext(os.path.basename(html))[0]
        if slug not in built:
            os.remove(html)
            print("  removed orphan rants/%s.html" % slug)

    posts.sort(key=lambda p: p["date"], reverse=True)

    if posts:
        rows = []
        for p in posts:
            rows.append(
                '      <li><a href="rants/%s.html">\n'
                '        <span class="rtitle">%s</span>\n'
                '        <span class="rdate">%s</span>\n'
                '        <span class="rsum">%s</span>\n'
                '      </a></li>' % (
                    p["slug"], escape(p["title"]),
                    escape(p["date_display"]), escape(p["summary"])))
        listing = '    <ul class="rlist">\n' + "\n".join(rows) + "\n    </ul>"
    else:
        listing = EMPTY_LIST

    index = INDEX_TEMPLATE.format(
        generated=GENERATED, fonts=FONTS, footer=FOOTER, list=listing)
    with open(os.path.join(HERE, "rants.html"), "w", encoding="utf-8") as f:
        f.write(index)
    print("  wrote rants.html (%d post%s)" % (len(posts), "" if len(posts) == 1 else "s"))


if __name__ == "__main__":
    print("building rants...")
    build()
    print("done.")
